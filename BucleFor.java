public class BucleFor {
    public static void main(String[] args) {
        //ecrire un programm pour trouver la somme des nombres entiers de 1 a 1000
        int somme = 0;       
        for (int i = 0; i < 1000; i++) {
            somme+=i;
        }
        System.out.println("la somme des entiers 1 a 1000 est: "+ somme);
    }
}
